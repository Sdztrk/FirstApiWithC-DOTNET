var builder = WebApplication.CreateBuilder(args);

// Swagger imports
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen(c =>
{
    c.SwaggerDoc("v1", new Microsoft.OpenApi.Models.OpenApiInfo { Title = "Hello World API with DOTNET", Version = "v1" });
});

var app = builder.Build();

// Middleware for Swagger
app.UseSwagger();
app.UseSwaggerUI(c =>
{
    c.SwaggerEndpoint("/swagger/v1/swagger.json", "Hello World API V1");
});

//Get read operation for CRUD
app.MapGet("/", () => "Hello World").WithName("HelloWorldFirstApiWithDOTNET");

//Listening/running the app
app.Run();




